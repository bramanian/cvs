          <h1>PRODUCTS</h1>
          <img src="images/itchology.png" alt="Itchology Mobile App" class="trad-mark">
          <p>The flagship product, Itchology™ is a highly unique mobile 
app for pet owners to help their veterinarian manage their itchy dog. 
Itch is one of the most common maladies of dogs, but the tools for 
veterinarians to measure itch severity are currently very limited. The 
Itchology app not only makes it easy for an owner to track their dog’s 
itch, treatments, diet, and flea exposure, but also uses a proprietary 
algorithm to analyze the influence of environmental factors that may be 
triggers of allergy flare ups. Owners of itchy dogs are empowered to 
actively contribute to their pets’ medical care with this powerful tool.
 Veterinarians can view data from individual patients or averages of all
 the Itchology user-patients under their care.</p>
