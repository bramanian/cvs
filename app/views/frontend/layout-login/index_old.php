with a beautifully designed mobile app interface that facilitates quick 
grading of skin lesion severity in dogs. Veterinarians can accurately 
track a patient’s improvement with their choice of two validated lesion 
severity indices CADLI (Canine Atopic Dermatitis Lesion Index) or 
CADESI-4 (Canine Atopic Dermatitis Extent and Severity Index – 4). By 
grading dogs’ skin lesion severity, veterinarians are able to more 
accurately evaluate improvement or worsening of their condition.</p>
